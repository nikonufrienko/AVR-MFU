void beep()
{
	cli();
	for(int i=0;i<100;i++)
	{
		SPK_PORT|=(1<<SPK);
		_delay_us(500);
		SPK_PORT&=~(1<<SPK);
		_delay_ms(1);
	}
	sei();
}
